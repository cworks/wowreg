    /**
     * Return an email address in the form <prefix>@<randomString>.com
     * @param prefix
     * @return
     */
    public static String getEmailAddress(String prefix) {
        return (prefix + "@" + RandomStringUtils.randomAlphabetic(8) + ".com").toLowerCase();
    }

    /**
     * Return a random String that has a length between 1 & 32 characters by default
     * @return
     */
