    private static final Random random = new Random(System.currentTimeMillis());

    private static class Alphabet {
        public static String[] uppercase() {
            String[] lc = new String[26];
            int i = 0;
            for(char c = 'A'; c <= 'Z'; c++) {
                lc[i++] = Character.toString(c);
            }
            return lc;
        }
        public static String[] lowercase() {
            String[] uc = new String[26];
            int i = 0;
            for(char c = 'a'; c <= 'z'; c++) {
                uc[i++] = Character.toString(c);
            }
            return uc;
        }
    }

    /**
     * Return a random Integer between [start, end], note that both ends are included
     * @param start
     * @param end
     * @return
     */
    public static int randomIntegerBetween(int start, int end) {
        int temp = 0;
        if(start > end) {
            temp = end;
            end = start;
            start = temp;
        }
        int length = end - start + 1;
        int offset = (int)(length * random.nextDouble());

        return start + offset;
    }

    /**
     * Return a random Long between [start, end], note that both ends are included
     * @param start
     * @param end
     * @return
     */
    public static long randomLongBetween(long start, long end) {
        long temp = 0;
        if(start > end) {
            temp = end;
            end = start;
            start = temp;
        }
        long length = end - start + 1;
        long offset = (long)(length * random.nextDouble());

        return start + offset;
    }

    /**
     * Returns a random boolean according to java.util.Random
     * @return
     */
    public static boolean randomBoolean() {
        return random.nextBoolean();
    }

    /**
     * Return a random 24 hour clock time string
     * @return
     */
    public static String randomTime() {
        int h = randomIntegerBetween(0, 23);
        int m = randomIntegerBetween(0, 59);
        int s = randomIntegerBetween(0, 59);
        String t = String.format("%02d:%02d:%02d", h, m, s);
        return t;
    }

    /**
     * Returns a random Date after the date passed into this method
     * @param after
     * @return
     */
    public static Date randomDateAfter(Date after) {
        Calendar cal = Calendar.getInstance();
        cal.setTime(after);
        long start = cal.getTimeInMillis();
        long end = System.currentTimeMillis();
        return new Date(randomLongBetween(start, end));
    }

    /**
     * Returns a random Date between a given startDate and endDate
     * @param startDate
     * @param endDate
     * @return a Date between startDate and endDate inclusive
     */
    public static Date randomDateBetween(Date startDate, Date endDate) {

        Calendar cal = Calendar.getInstance();
        cal.setTime(startDate);
        long start = cal.getTimeInMillis();
        cal.setTime(endDate);
        long end = cal.getTimeInMillis();

        return new Date(randomLongBetween(start, end));
    }

    /**
     * Randomly generate a phone number in this form 123-456-7890 by default.
     * @return
     */
    public static String randomPhoneNumber() {
        return randomIntegerBetween(100, 999) + "-"
            + randomIntegerBetween(100, 999) + "-"
            + randomIntegerBetween(1000, 9999);
    }

    /**
     * Return a random first name, with approximately a 50-50 chance that it's a female/male name.
     * @return
     */
    public static String randomFirstName() {
        Boolean gender = randomBoolean();
        if(gender == true) {
            return femaleFirstNames[randomIntegerBetween(0, femaleFirstNames.length - 1)];
        } else {
            return maleFirstNames[randomIntegerBetween(0, maleFirstNames.length - 1)];
        }
    }

    /**
     * Return a random last name
     * @return
     */
    public static String randomLastName() {
        return lastNames[randomIntegerBetween(0, lastNames.length - 1)];
    }

    /**
     * Return an email address in the form <prefix>@<randomString>.com
     * @param prefix
     * @return
     */
    public static String randomEmailAddress(String prefix) {
        return (prefix + "@" + RandomStringUtils.randomAlphabetic(8) + ".com").toLowerCase();
    }

    /**
     * Return a random String that has a length between 1 & 32 characters by default
     * @return
     */
    public static String randomString() {
        return RandomStringUtils.random(32, true, false);
    }

    /**
     * Return a random String with prefix
     * @param prefix
     * @return
     */
    public static String randomString(String prefix) {
        return prefix + randomString();
    }

    /**
     * Randomly generate a password.
     * @return
     */
    public static String randomPassword() {
        return null;
    }

    /**
     * Randomly return a character from the alphabet, by default returns mixed case characters
     * @return
     */
    public static String randomAlphabetCharacter() {
        Boolean uppercase = randomBoolean();
        if(uppercase) {
            return Alphabet.uppercase()[randomIntegerBetween(0, 25)];
        } else {
            return Alphabet.lowercase()[randomIntegerBetween(0, 25)];
        }
    }

